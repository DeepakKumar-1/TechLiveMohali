package com.company.Week1;
import java.lang.*;
public class JavaLangLibrary {
    public static void main(String[] args) {
        System.out.println(Math.max(12, 34));
        System.out.println(Math.min(2,3));
        System.out.println(Math.abs(-4656));
        System.out.println(Math.sqrt(246));
        System.out.println(Math.cbrt(246)); // cube Root
        System.out.println(Math.ceil(4.56));
        System.out.println(Math.pow(2, 4));
        System.out.println(Math.random());
        System.out.println(Math.addExact(23, 45));
        System.out.println(Math.subtractExact(44, 5));
        System.out.println(Math.multiplyExact(44, 5));
        System.out.println(Math.floorDiv(55, 5));
        System.out.println(Math.asin(45));
        System.out.println(Math.sin(90));
        System.out.println(Math.tan(90));
        System.out.println(Math.atan(90));
    }
}
